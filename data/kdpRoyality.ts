export function getEstSales(country: number, bsr: number) {
  let collection = BSR_VALUES[country];

  let input = Number(bsr);
  let maxPotSale, minPotSale, maxPotBSR, minPotBSR;

  for (let i in BSR_VALUES[0]) {
    let bsr_point = BSR_VALUES[0][i];
    if (bsr_point < bsr) {
      maxPotBSR = bsr_point;
      minPotBSR = BSR_VALUES[0][Number(i) - 1];
      maxPotSale = collection[i];
      minPotSale = collection[Number(i) - 1];
      break;
    }
  }

  let bsr_formula =
    maxPotSale -
    ((input - maxPotBSR) / (minPotBSR - maxPotBSR)) * (maxPotSale - minPotSale);
  return bsr_formula;
}

